import Foundation
import UIKit


extension UIColor {
    
    class func themeColor() -> UIColor {
        return UIColor(red: 255/255, green: 181/255, blue: 0/255, alpha: 1.0)
    }
    
    class func backgroundColor() -> UIColor {
        return UIColor(red: 236/255, green: 235/255, blue: 243/255, alpha: 1.0)
    }
    
}